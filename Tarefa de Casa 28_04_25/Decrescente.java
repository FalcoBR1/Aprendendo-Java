public class Decrescente {
    public static void main(String[] args) {
        
        /*Crie um algoritmo que imprima os números de 10 a 1 em ordem
        decrescente. 
        */

        //(inicialização; condição; incremento/decremento)
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
    }

}